C – Introdução ao COBIT 5 Implementation (IM): 7 questões (14%);

Conceitos:

Governança corporativa de TI
- O sistema pelo qual o uso atual e futuro da TI é dirigido e controlado
- Envolve a avaliação, a direção do uso da TI e monitora seu uso para realizar os planos
- Dá suporte à organização no alcance de seus objetivos estratégicos
- Inclui a estratégia e as políticas para o uso de TI dentro de uma organização
- ISO 38500

Gestão de TI
- Sistema de controles e processos necessários para alcançar os objetivos estratégicos 
estabelecidos pela direção da organização

Criação de valor
Obtenção de benefícios por meio da otimização do uso de recursos e dos riscos a um nível aceitavel

Foco em governança corporativa de TI deixando claro a distinção entre governança e gestão
Fundamentado em 5 princípios que permitem que a organização contrua um framework efetivo de governança
e gestão de TI

Baseado em 7 enablers -> facilitadores, habilitadores, viabilizadores que otimizam investimentos em
tecnologia e informação utilizados para o benefíco dos stakeholders

Possui 37 processos, 5 processos de governança, 32 processos de gestão
São desdobrados em práticas de governança e práticas de gestão

Processos
- Identificação do processo
- Descrição - uma visão geral do que o processo faz e como o processo alcança seu propósito
- Propósito do processo - descrição geral do propósito do processo 
- Informação de objetivos em cascata
- Objetivos de processos e métricas
- Matriz RACI
- Descrição detalhada de práticas de processo (Título da prática e descrição, entradas e saídas
da prática , atividades de processo)
- Guias relacionados (Related guidance)

Integra todos os conhecimentos de diferentes frameworks da ISACA
Cobit 4.1
- Val IT (Valor de ti para o negócio)
- Risk IT (risco relacionado ao uso de TI)
- BMIS (segurança)
- IT Assurance Framework  (ITAF)
- Taking Governance Forward (TGF)
- Board Briefing on IT Governance 2nd Edition


D - Modelo de Avaliação de Capacidade de Processos (PC): 7 questões (14%).

Modelo de Capacidade de Processos

- É baseado na norma ISO/IEC 15504 de Engenharia de Software
- Cada nível de capacidade de processo possui um conjunto de atributos de processo que devem ser 
avaliados para o alcance do nível em questão.
- 6 níveis de capacidade
- 9 atributos de processo
- Ciclo de vida de melhoria contínua (anel interno)
- Capacitação da mudança (anel intermediário)
- Gestão do programa (anel externo)
- 3 componentes inter-relacionados
- Possui 7 fases