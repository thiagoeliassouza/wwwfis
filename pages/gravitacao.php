    <p><h2>Um breve histórico da Astronomia</h2></p>

    <p>A Astronomia é uma das mais antigas ciências. Há registros das fases da lua marcada em ossos datadas de 30.000ac. A Astronomia sempre foi importante para o homem para entender os ciclos de plantio, colheita, períodos das chuvas e calendário.</p>

    <p><h2>Astronomia Grega</h2></p>

    <p>
      O apogeu da ciência antiga se deu na Grécia entre 600ac e 400dc. Principais características:<br>
      <li>Conceito da esfera de cristal</li>
      <li>Geocentrismo</li>
    </p>

    <p><h2>Astrônomos Notáveis</h2><br></p>

    <p><h2>Tales de Mileto (~624-546ac)</h2><br>
      <li>Introduziu na Grécia antiga os fundamentos da Astronomia trazidas do Egito</li>
      <li>Conhecia a curvatura da Terra</li>
      <li>Sabia que a Lua era iluminada pelo Sol</li>
      <li>Chegou a prever um Eclipse em 584ac.</li>
    </p>

    <p><h2>Pitágoras de Samos (~572-497ac)</h2><br>
      <li>Defendia a esfericidade da Terra, da Lua e de outros corpos celestes</li>
      <li>Foi o primeiro a chamar o céu de Cosmos</li>
    </p>

    <p><h2>Aristóteles de Estagira (384-322ac)</h2><br>
      <li>Defendeu a esfericidade dos astros</li>
      <li>Explicou as fases da Lua</li>
      <li>Afirmava que o Universo era finito e esférico, ou não teria centro e não poderia se mover</li>
    </p>

    <p><h2>Aristarco de Samos (310-230ac)</h2><br>
      <li>Viveu boa parte da vida em Alexandria</li>
      <li>Temos um relato de Arquimedes que diz: "Aristarco publicou um livro dizendo que o Universo é muitas vezes maior do que se afirma. As estrelas fixas e o Sol se mantém imóveis e a Terra gira em torno do Sol em círculos".</li>
    </p>

    <p><h2>Erastóstenes de Cirene (276-194ac)</h2><br>
      <li>Talvez tenha sido o primeiro a usar o globo para representar a Terra</li>
      <li>Fez várias cartas geográficas e um tratado sobre a posição das estrelas</li>
      <li>Mediu o diâmetro da Terra</li>
    </p>

    <p><h2>Hiparco de Nicéia (190-125ac)</h2><br>
      <li>Fez um mapa com mais de 850 estrelas</li>
      <li>Deduziu a precessão da Terra</li>
      <li>Distrência da Lua até a Terra era 59R da Terra</li>
      <li>Calculou a duração do ano com erro de 6 minutos</li>
    </p>

    <p><h2>Claudius Ptolomeu (85-165ac)</h2><br>
      <li>Obra: Almagesto</li>
      <li>Sistema geocêntrico e introduziu o conceito de epiciclo</li>
      <li>Foi um grande sistematizador</li>
    </p>

    <p><h2>Astronomia na Idade Média</h2></p>

    <p><h2>Tabelas Afonsinas</h2><br>
      <li>Afonso X, o sábio, rei de Castela (Espanha), em 1252</li>
      <li>Contratou 50 astrônomos para revisar as tabelas astronômicas de Ptolomeu</li>
      <li>Este conteúdo foi anexado ao Almagesto (150dc)</li>
    </p>

    
    <p><h2>Nicolau Copérnico (1473-1543</h2><br>
      <li>Apresentou o sistema Heliocêntrico</li>
      <li>Manteve as ideias de perfeição dos corpos e dos movimentos dos antigos</li>
      <li>Colocou o Sol no centro das esferas celestes</li>
    </p>

    <p><h2>Tycho Brahe (1546-1601)</h2><br>
      <li>Encontrou erros nas tabelas Afonsinas</li>
      <li>Descobriu anomalia nas órbitas dos planetas</li>
      <li>Estudou muito o movimento do planeta Marte</li>
      <li>Último grande Astrônomo da era pré-telescópica</li>
    </p>

     <p><h2>Galileu Galilei (1564-1642)</h2><br>
      <li>Melhorou muito o telescópio</li>
      <li>Descobriu montanhas na Lua</li>
      <li>Descobriu, em 1610, os quartro maiores satélites de Jupiter</li>
      <li>Fez os primeiros registros das manchas solares e dos anéis de Saturno</li>
      <li>É considerado o "Pai da ciência moderna"</li>
    </p>

    <p><h2>Johannes Kepler (1571-1630)</h2><br>
      <li>Propôs as 3 leis que regem o movimento planetário</li>
      <li>1ª Lei de Kepler: Lei das órbitas</li>
      <li>2ª Lei de Kepler: Lei das áreas</li>
      <li>3ª Lei de Kepler: Lei dos períodos</li>
    </p>

